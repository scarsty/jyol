function GetEngStr(word)--ÊäÈëÆ´Òô
	local T3={"a","b","c","d","e","f","g","h","i","j","k","l","m","n","o","p","q","r","s","t","u","v","w","x","y","z"}
	--local T3={"1","2","3","4","5","6","7","8","9"};
