function NumtoChs(n)
	local T={[0]="","一","二","三","四","五","六","七","八","九","十","十一","十二","十三","十四","十五","十六","十七","十八","十九","廿"};
	local str="";
	while n>=10 do
		str=T[n%10]..str;
		n=math.modf(n/10);
	end
	str=T[n%10]..str;
	return str;
end
function EndlessMain(E_id)
	local M_id
	while true do
		if E_id==0 then
			M_id=EndlessInitial();
			Endless_E000(M_id)
		elseif E_id%5==0 then
			JY.SubScene=M_id;
			Init_SMap(2);
			say("当前第"..NumtoChs(E_id).."轮，特殊剧情。".."ＨCurrent is the "..E_id.."th, special event.",0);
		else
			JY.SubScene=M_id;
			Init_SMap(2);
			say("当前第"..NumtoChs(E_id).."轮，普通剧情。".."ＨCurrent is the "..E_id.."th, normal event.",0);
		end
		E_id=E_id+1;
		M_id=EndlessNextGo(M_id);
	end
	
end
function EndlessNextGo(M_id)
	say("接下来去哪儿呢？",0);
	local menu={};
	for i=1,EndlessNet[M_id].index do
		menu[i]={JY.Scene[EndlessNet[M_id][i]]["名称"]};
	end
	local sel=GenSelection(menu);
	return EndlessNet[M_id][sel];
end
function EndlessInitial()
	DrawStrBoxWaitKey("请选择所属门派",C_WHITE,CC.Fontbig);
	local menu={};
	for i,v in pairs(Endless_Force) do
		menu[i]={JY.Scene[v]["名称"]};
	end
	local sel=GenSelection(menu);
	JY.SubScene=Endless_Force[sel];
	say("初始化完毕ＨInitialization OK",0);
	JY.Status=GAME_SMAP;
	JY.MmapMusic=-1;
	return JY.SubScene;
end
