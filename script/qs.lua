local function wrongstr(str)
	local len=string.len(str)
	local newstr="";
	local changed=false
	for i=1,len,2 do
		local s=string.sub(str,i,i+1)
		if cbz[s]~=nil then
			if math.random(10)<3 then
				local n=cbz[s][0]
				if n==1 then
					s=cbz[s][1]
				else
					s=cbz[s][math.random(n)]
				end
				changed=true
			end
		end
		newstr=newstr..s
	end
	if changed then
		return newstr
	else
		return wrongstr(str)
	end
end
